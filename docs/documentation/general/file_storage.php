<h2>File Storage</h2>

<p>Students can access a personal file management area by following the <em>File Storage</em> link from the Home page or main navigation. There are several different workspaces for storing files, each differently accessible to others in the course:
	<ul>
		<li><em>Course Files</em> - the default workspace. User files are available for download by other course members and can be managed by the instructor or assistants with file storage privileges.</li>
		<li><em>My Files</em> - private files only the user can manage and access.</li>
		<li><em>Assignments</em> - user's assignment submission workspace only accessible to instructors or assistants with assignment privileges.</li>
		<li><em>Group</em> - shared files managed by assistants and instructors and accessible to all group members, perhaps for the purposes of collaborative group work.</li>
	</ul>

To manage a different workspace, select the preferred storage area from the dropdown and use the <code>Workspace</code> button.
</p>

<p>For better organisation, separate folders can be created using the <em>Create Folder</em> feature at the top right of the file area. To upload a new file with a description of its contents, use the <em>New File</em> feature at the top left. The file will be uploaded to the current location, therefore to store the file in a specific folder, open the folder first and then upload the new file. It is also possible to <code>Move</code> files around within a workspace, <code>Download</code> files to the harddrive, <code>Edit</code> file details and rename directories and <code>Delete</code> files and directories.</p>

<p>To view a file, it must be downloaded and opened locally.</p>


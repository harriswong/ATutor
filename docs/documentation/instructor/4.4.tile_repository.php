<h2>4.4 TILE Repository</h2>
	<p>Content created in ATutor can be exported to <em>The Inclusive Learning Exchange</em> (TILE) using the <em>Export</em> tool on the Import/Export Content screen. Content can also be imported from the TILE repository by entering a search term into the <em>TILE Repository Search</em>, then choosing <em>Import</em> next to a listing in the search results.  Choose the <em>Preview </em>link next to a search result to open the TILE content browser, or choose <em>Download</em>, to retrieve the content package from TILE repository. Once downloaded, the retrieved package can be imported into ATutor using the Import/Export Content import tool. </p>
	
	<p>Visit the TILE Web site for more information about using the repository, and to set up an account on the system. (<a href="http://www.inclusivelearning.ca/" target="new">The Inclusive Learning Exchange</a> )</p>



<h2>7. File Manager</h2>
	<p>ATutor has a file system used for storing course resource files, that is managed using the <em>File Manager</em>. The File Manager is one of the main ways of getting files associated with ATutor created content into your course. The File Manager also allows you to create, edit, move, and delete files.</p>

	<p>The File Manager can be found under the <em>Manage</em> tab, linked from the Content Editor so it can be opened while authoring ATutor content pages, or linked throughout the Test Question authoring screens so files can be managed while assembling tests.</p>


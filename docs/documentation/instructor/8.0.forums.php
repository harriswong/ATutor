<h2>8. Forums</h2>
	<p>A Forum is a student tool that allows all course members to communicate in a structured manner through messages. Being a <a href="./14.0.student_tools.php">Student Tool</a>, the forums can be enabled, disabled, and linked from the main navigation, the course home page, or displayed as a menu module as choosen in the Student Tools preferences.</p>

	<p>Course instructors and privileged student users can delete, lock, and stick threads and messages.</p>

